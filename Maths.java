
public class Maths {

	public void sum (int a,int b)
	{
		int c =a+b;
		System.out.println("Sum of  your number:" + c);
	}
	
	public void sub (int a,int b)
	{
		int c =a-b;
		System.out.println("Subtraction of  your number:" + c);
	}
	
	public void multi (int a,int b)
	{
		int c =a*b;
		System.out.println("Multiplication of  your number:" + c);
	}
	
	public void div (int a,int b)
	{
		int c =a/b;
		System.out.println("Division of the your number:" + c);
	}
}
